    warning "‚ö†Ô∏è  –°–∏—Å—Ç–µ–º–∞ –º–æ–∂–µ—Ç –±—ã—Ç—å –µ—â–µ –Ω–µ –≥–æ—Ç–æ–≤–∞, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –ª–æ–≥–∏:"
    docker-compose -f $COMPOSE_FILE -p $PROJECT_NAME logs --tail=10
